// Loop in JS
// for loop

for (let i = 1; i <= 10; i++){
    console.log(i, "Hello World");
}



// while loop
let num = 1; 
while (num <= 10){
    console.log("Number is", num);
    num++;
}



// do while loop
let a = 1;
do {
    console.log(a);
    a++;
}while (a <= 10);
